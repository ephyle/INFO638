<p>
This website contains metadata harvested with OAI-PMH and provides a search interface to access those resources. The scope of harvested resources are Library and Information Science related repositories, primrary found through <a href= "https://v2.sherpa.ac.uk/opendoar/">OpenDOAR</a> (Directory of Open Access Repositories). I have only added a handfull of repositories, however others can contribute to endpoints which can be done through the "Log-in to add endpoint" link in the navigation bar. If you know of a OAI-PMH Library and Information Science related academic repository, please reach out to <a href="mailto:elizabethphyle@gmail.com">elizabethphyle@gmail.com</a> and I will supply you with login credentials. </p>

<p> There are various levels of investment in OAI-PMH across disciplines, institutions, and countries. Through the course of this project it has been my impression that there is more disciplinary participation from the hard sciences and in European and Latin American countries. Library Science specific repositories in English were particularly hard to come by. Of those listed in OpenDOAR the majority either were now unavailable or the repository was OpenAccess but not a OAI-PMH participant. 
</p>

<p>The Open Archives Initiative Protocol for Metadata Harvesting is a significant step in creating a pipeline for open access resources between repositories and other discovery resources mechanisms like search engines. While there are still barriers with this approach and OAI-PMH is not adopted in every field nor across all open access repositories, there have been concerted efforts to innovate within OAI-PMH to extend its potential. </p>

